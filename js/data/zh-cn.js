function buildZhCnTranslations(source) {
    const charMap = {
        '體':'体','綠':'绿','錄':'录','顯':'显','讀':'读','閱':'阅','環':'环','響':'响','裝':'装',
        '經':'经','濟':'济','擴':'扩','變':'变','廣':'广','斷':'断','衛':'卫','愛':'爱','灣':'湾',
        '緣':'缘','線':'线','錶':'表','濾':'滤','處':'处','積':'积','規':'规','實':'实',
        '專':'专','應':'应','聲':'声','隨':'随','覽':'览','區':'区','鄉':'乡','臺':'台','網':'网','證':'证',
        '觸':'触','術':'术','審':'审','領':'领','雕':'雕','輸':'输','鋪':'铺','鋼':'钢','鎖':'锁',
        '頁':'页','葉':'叶','棒':'棒','測':'测','項':'项',
        '煩':'烦','樹':'树','潤':'润','靈':'灵','勝':'胜','憑':'凭','顧':'顾','楊':'杨',
        '續':'续','難':'难','驗':'验','顏':'颜','習':'习','憶':'忆','頻':'频','麗':'丽','順':'顺',
        '緩':'缓','靜':'静','齊':'齐','綜':'综','纖':'纤','螢':'萤',
        '覺':'觉','觀':'观','護':'护','點':'点','燈':'灯','緊':'紧','織':'织','繞':'绕','軟':'软',
        '輕':'轻','軌':'轨','輔':'辅','辦':'办','載':'载','轉':'转','輝':'辉','農':'农','遺':'遗','遠':'远',
        '醜':'丑','鎮':'镇','鐵':'铁','關':'关','陣':'阵','陸':'陆','隊':'队','願':'愿','頓':'顿',
        '頤':'颐','颱':'台','鐘':'钟','離':'离','韌':'韧','須':'须','馳':'驰','驟':'异常','驅':'驱','盡':'尽'
    };
    const replaceAll = (value) => {
        if (typeof value !== 'string') return value;
        let result = '';
        for (const ch of value) {
            result += charMap[ch] || ch;
        }
        return result;
    };
    const result = {};
    for (const key in source) {
        result[key] = replaceAll(source[key]);
    }
    return result;
}
window.translations['zh-CN'] = buildZhCnTranslations(window.translations.zh);